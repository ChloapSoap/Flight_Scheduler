    private void addDayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDayButtonActionPerformed
        int result = queryHandler.addNewDate(addDayText.getText());
        
        if (result == 1) {
            bookDateCBox.addItem(addDayText.getText());
            statusDateCBox.addItem(addDayText.getText());
            statusDateCBox2.addItem(addDayText.getText());
            cancelDateCBox.addItem(addDayText.getText());
            addOutput.setText(addOutput.getText() + "Date has been added successfully\n");
        }
        else {
            addOutput.setText(addOutput.getText() + "Error! Date has not been added\n");
        }
    }//GEN-LAST:event_addDayButtonActionPerformed

