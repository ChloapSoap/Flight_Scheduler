    private void loadDates() {
        try {
            Dates = queryHandler.getAllDates();
            dateEntries = Dates.size();
            
            for (int i = 0; i < dateEntries; i++) {
                bookDateCBox.addItem(Dates.get(i).getFlightDate());
                statusDateCBox.addItem(Dates.get(i).getFlightDate());
                statusDateCBox2.addItem(Dates.get(i).getFlightDate());
                cancelDateCBox.addItem(Dates.get(i).getFlightDate());
            }
        }
        catch (Exception e) {
            e.printStackTrace();
        }
    }
    
